#include "libft.h"
#include <stddef.h>

void	ft_bzero(void *s, size_t len)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = s;
	while (i < len)
	{
		str[i] = '\0';
		i++;
	}
}
